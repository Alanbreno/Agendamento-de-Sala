    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
       if (!"".equals(txtIdentificação.getText()) && !"".equals(txtNdeAlunos.getText()) && !"".equals(txtSemetre.getText()) && (botaoRadioSim.isSelected() == true || botaoRadioNao.isSelected() == true) ){
        Entidades.Turma t = new Entidades.Turma();
        TurmaJpaController j = new TurmaJpaController();
        t = j.findTurma(Integer.parseInt(txtID.getText()));
        t.setTurmaTurno(txtTurno.getText());
            t.setTurmaNumAluno((short)Integer.parseInt(txtNdeAlunos.getText()));
            t.setTurmaSemestre((short)Integer.parseInt(txtSemetre.getText()));
            t.setTurmaCodigo(txtIdentificação.getText());
            if (botaoRadioSim.isSelected() ==  true){
                t.setTurmaStatus(true);
            }else{
                t.setTurmaStatus(false);
            }
           try {
               j.edit(t);
               
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
           } catch (Exception ex) {
               Logger.getLogger(TelaEditarTurmas.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
    }//GEN-LAST:event_botaoSalvarActionPerformed

