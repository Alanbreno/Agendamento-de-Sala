    public void editar(String valor){
        TurmaJpaController j = new TurmaJpaController();
         Entidades.Turma  p = new Entidades.Turma();
         List<Turma> a = j.FiltroTurma(valor);
         p = a.get(0);
         txtIdentificação.setText(p.getTurmaCodigo());
         txtNdeAlunos.setText(String.valueOf(p.getTurmaNumAluno()));
         txtSemetre.setText(String.valueOf(p.getTurmaSemestre()));
         txtTurno.setText(p.getTurmaTurno());
         txtID.setText(p.getTurmaId().toString());
         if(p.getTurmaStatus()==true){
             botaoRadioSim.setSelected(true);
         }else{
             botaoRadioNao.setSelected(true);
         }
         
    }
