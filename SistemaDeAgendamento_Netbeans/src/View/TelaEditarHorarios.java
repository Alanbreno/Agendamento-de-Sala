public void editar(Date horario){
    
      SimpleDateFormat data = new SimpleDateFormat("HH:mm");
     HorarioJpaController j = new HorarioJpaController();
     Entidades.Horario p = new Entidades.Horario();
 String hora = data.format(horario);
        try {
            Date teste = (Date) data.parse(hora);
            List<Horario> a;
 a = j.FiltroHora(teste);
 p = a.get(0);
 txtInicio.setText(p.getHorarioInicial().toString());
 txtAte.setText(p.getHorarioFinal().toString());
 txtID.setText(p.getHorarioId().toString());
        } catch (ParseException ex) {
            Logger.getLogger(TelaEditarHorarios.class.getName()).log(Level.SEVERE, null, ex);
        }
 
 
 


     
}
